# ============================================================================

def score(target_path: Path) -> dict[str, Any]:
    criteria = {
        "agent-context.json": (10, (target_path / "agent-context.json").exists()),
        "CLAUDE.md": (10, (target_path / "CLAUDE.md").exists()),
        "AGENTS.md": (10, (target_path / "AGENTS.md").exists()),
        "system_prompt.md": (5, (target_path / "system_prompt.md").exists() or (target_path / "agents" / "system_prompt.md").exists()),
        "tools/ has files": (10, any((target_path / "tools").glob("*")) if (target_path / "tools").exists() else False),
    }
    context_file = target_path / "agent-context.json"
    if context_file.exists():
        try:
            ctx = json.loads(context_file.read_text())
            static = ctx.get("static", ctx)
            entry_point = static.get("entry_point", "")
            criteria["entry_point exists"] = (10, bool(entry_point) and (target_path / entry_point).exists())
            criteria["test_command set"] = (10, bool(static.get("test_command") or ctx.get("commands", {}).get("test")))
            criteria["restricted_write_paths"] = (10, len(static.get("restricted_write_paths", [])) > 0)
            criteria["environment_variables"] = (10, len(static.get("environment_variables", [])) > 0)
            criteria["domain_concepts >= 3"] = (5, len(static.get("domain_concepts", [])) >= 3)
        except json.JSONDecodeError:
            pass
    criteria["OpenAPI spec"] = (5, any([
        list(target_path.glob("**/openapi.yaml")), list(target_path.glob("**/openapi.yml")),
        list(target_path.glob("**/openapi.json")), list(target_path.glob("**/swagger.yaml")),
        list(target_path.glob("**/swagger.yml")), list(target_path.glob("**/swagger.json")),
    ]))
    criteria["CI config exists"] = (5,
        any(target_path.glob(".github/workflows/*.yml"))
        or any(target_path.glob(".gitea/workflows/*.yml"))
        or (target_path / ".travis.yml").exists()
        or (target_path / ".circleci/config.yml").exists()
        or (target_path / "Jenkinsfile").exists()
    )
    total = 0
    table_rows = []
    for criterion, (points, achieved) in criteria.items():
        if achieved:
            total += points
            table_rows.append((f"✅ {criterion}", f"+{points}"))
        else:
            table_rows.append((f"⬜ {criterion}", "+ 0"))
    return {"score": total, "max": 100, "rows": table_rows}


# ============================================================================
# LLM-First Pipeline (--llm flag)
# ============================================================================

def _run_llm_pipeline(
    target: Path,
    only: str | None,
    dry_run: bool,
    force: bool,
    quiet: bool,
) -> list[tuple[str, str]]:
    api_key = os.environ.get("ANTHROPIC_API_KEY")
    if not api_key:
        print("❌ ANTHROPIC_API_KEY environment variable is not set.")
        sys.exit(1)
    try:
        import anthropic
        from agent_ready import analyser, generator
    except ImportError as exc:
        print(f"❌ Missing dependency for LLM mode: {exc}")
        print("   Run: pip install anthropic")
        sys.exit(1)
    client = anthropic.Anthropic(api_key=api_key)
    if not quiet:
        print("\n🤖 LLM-first mode active")
        print("─" * 50)
    analysis = analyser.run(target, client, quiet=quiet)
    if not quiet:
        print("\n✍️  Generating files with Claude Sonnet...")
    gen = generator.LLMGenerator(target=target, analysis=analysis, client=client, dry_run=dry_run, force=force, quiet=quiet)
    if only:
        return gen.generate_only(only)
    return gen.generate_all()


# ============================================================================
# Eval Pipeline (--eval / --eval-only flags)
# ============================================================================

def _run_eval_pipeline(
    target: Path,
    fail_level: float = 0.0,
    quiet: bool = False,
) -> dict[str, Any]:
    """
    Run evaluation: measure whether generated context files improve AI responses.
    Asks 9 questions with and without context, judges both, reports improvement delta.
    """
    api_key = os.environ.get("ANTHROPIC_API_KEY")
    if not api_key:
        print("❌ ANTHROPIC_API_KEY environment variable is not set.")
        sys.exit(1)
    try:
        import anthropic
        from agent_ready import evaluator
    except ImportError as exc:
        print(f"❌ Missing dependency for eval mode: {exc}")
        print("   Run: pip install anthropic")
        sys.exit(1)
    client = anthropic.Anthropic(api_key=api_key)
    try:
        result = evaluator.run_eval(target=target, client=client, fail_level=fail_level, quiet=quiet)
        report_path = evaluator.save_eval_report(target, result)
        if not quiet:
            print(f"\n  📄 Full report saved: {report_path.relative_to(target)}")
        if fail_level > 0 and not result["passed"]:
            print(f"\n❌ Eval failed: pass rate {int(result['pass_rate'] * 100)}% is below threshold {int(fail_level * 100)}%")
            sys.exit(1)
        return result
    except ValueError as e:
        print(f"❌ {e}")
        sys.exit(1)


