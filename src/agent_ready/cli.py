#!/usr/bin/env python3
"""
AgentReady — CLI Transformer

Transforms any repository into an AI-agent-ready codebase by detecting:
- Languages, frameworks, and build systems
- Entry points and test frameworks
- Environment configuration
- Code conventions

Then generates:
- agent-context.json (machine-readable metadata)
- AGENTS.md (GitHub Copilot / OpenAI instructions)
- CLAUDE.md (Claude Code instructions)
- system_prompt.md (Universal LLM system prompt)
- mcp.json (MCP server configuration)
- Tool templates (language-specific)
- memory/schema.md (domain-specific memory schema)

Usage:
  python run_transformer.py --target /path/to/repo
  python run_transformer.py --target /path/to/repo --llm
  python run_transformer.py --target /path/to/repo --only agents
  python run_transformer.py --target /path/to/repo --dry-run
  python run_transformer.py --target /path/to/repo --llm --eval
  python run_transformer.py --target /path/to/repo --eval-only
  python run_transformer.py --target /path/to/repo --eval-only --fail-level 0.8

Generated by AgentReady (agent-ready) v2.0.0
"""

from __future__ import annotations

import argparse
import json
import os
import sys
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

SCRIPT_DIR = Path(__file__).resolve().parent
TOOLKIT_ROOT = SCRIPT_DIR.parent.parent

_PKG_TEMPLATES = SCRIPT_DIR / "templates"
_REPO_TEMPLATES = TOOLKIT_ROOT / "templates"
TEMPLATES_DIR = _PKG_TEMPLATES if _PKG_TEMPLATES.is_dir() else _REPO_TEMPLATES

LANGUAGE_EXTENSIONS: dict[str, list[str]] = {
    "Python": [".py"],
    "TypeScript": [".ts", ".tsx"],
    "JavaScript": [".js", ".jsx", ".mjs", ".cjs"],
    "Java": [".java"],
    "Go": [".go"],
    "Rust": [".rs"],
    "C#": [".cs"],
    "Ruby": [".rb"],
    "C++": [".cpp", ".cc", ".cxx", ".hpp", ".h"],
    "C": [".c"],
    "PHP": [".php"],
    "Swift": [".swift"],
    "Kotlin": [".kt", ".kts"],
}

CONFIG_FILES: dict[str, dict[str, str]] = {
    "package.json": {"language": "JavaScript", "build": "npm"},
    "pyproject.toml": {"language": "Python", "build": "pip"},
    "requirements.txt": {"language": "Python", "build": "pip"},
    "go.mod": {"language": "Go", "build": "go"},
    "Cargo.toml": {"language": "Rust", "build": "cargo"},
    "pom.xml": {"language": "Java", "build": "maven"},
    "build.gradle": {"language": "Java", "build": "gradle"},
    "Gemfile": {"language": "Ruby", "build": "bundler"},
}

FRAMEWORK_INDICATORS: dict[str, list[str]] = {
    "React": ["react", "react-dom"],
    "Next.js": ["next"],
    "Vue": ["vue"],
    "Angular": ["@angular/core"],
    "Express": ["express"],
    "FastAPI": ["fastapi"],
    "Django": ["django"],
    "Flask": ["flask"],
    "Spring Boot": ["spring-boot-starter"],
    "Rails": ["rails"],
}

IGNORE_DIRS = {
    ".git", "node_modules", "__pycache__", ".venv", "venv", "env",
    ".tox", ".mypy_cache", ".pytest_cache", "dist", "build", "target",
    ".next", ".nuxt", "out", "coverage", ".idea", ".vscode",
}


