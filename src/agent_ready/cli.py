class AgenticGenerator:
    def __init__(self, target: Path, metadata: dict[str, Any], dry_run: bool = False, force: bool = False):
        self.target = target
        self.meta = metadata
        self.dry_run = dry_run
        self.force = force
        self.generated: list[tuple[str, str]] = []

    def generate_all(self) -> list[tuple[str, str]]:
        self._generate_context_json()
        self._generate_agents_md()
        self._generate_claude_md()
        self._generate_system_prompt()
        self._generate_mcp_json()
        self._generate_tool_templates()
        self._generate_openai_agent()
        self._generate_gemini_agent()
        self._generate_memory_schema()
        try:
            readiness = score(Path(self.target))
            print(f"AGENTIC READINESS SCORE: {readiness['score']} / {readiness['max']}")
        except Exception as e:
            print(f"[WARN] Could not calculate readiness score: {e}")
        return self.generated

    def _generate_openai_agent(self) -> None:
        template_path = TEMPLATES_DIR / "openai_agent.template.py"
        if template_path.exists():
            self._write_file("agents/openai_agent.py", template_path.read_text(encoding="utf-8"))

    def _generate_gemini_agent(self) -> None:
        template_path = TEMPLATES_DIR / "gemini_agent.template.yaml"
        if template_path.exists():
            self._write_file("agents/gemini_agent.yaml", template_path.read_text(encoding="utf-8"))

    def generate_only(self, category: str) -> list[tuple[str, str]]:
        dispatch = {
            "agents": lambda: (self._generate_agents_md(), self._generate_claude_md(), self._generate_system_prompt()),
            "tools": self._generate_tool_templates,
            "context": self._generate_context_json,
            "memory": self._generate_memory_schema,
        }
        fn = dispatch.get(category)
        if fn is None:
            print(f"❌ Unknown category: {category}")
            sys.exit(1)
        fn()
        return self.generated

    def _write_file(self, rel_path: str, content: str) -> str:
        full_path = self.target / rel_path
        already_exists = full_path.exists()
        if already_exists and not self.force:
            status = "⏭️  Skipped"
            self.generated.append((rel_path, status))
            print(f"  ⏭️  {rel_path} (already exists, use --force to overwrite)")
        else:
            if self.dry_run:
                status = "📝 (dry-run)"
            else:
                full_path.parent.mkdir(parents=True, exist_ok=True)
                full_path.write_text(content, encoding="utf-8")
                status = "🔄 Updated" if already_exists else "✅ Created"
            self.generated.append((rel_path, status))
            print(f"  {status[0:2]} {rel_path}")
        return status

    def _fill_template(self, template_content: str) -> str:
        m = self.meta
        replacements = {
            "{{PROJECT_NAME}}": m.get("project_name", "Project"),
            "{{PROJECT_DESCRIPTION}}": m.get("description", ""),
            "{{PRIMARY_LANGUAGES}}": ", ".join(m.get("primary_languages", [])) or "Unknown",
            "{{FRAMEWORKS}}": ", ".join(m.get("frameworks", [])) or "None",
            "{{BUILD_SYSTEM}}": m.get("build_system", "unknown"),
            "{{TEST_FRAMEWORK}}": m.get("test_framework", "unknown"),
            "{{TEST_DIRECTORY}}": m.get("test_directory", "tests/"),
            "{{INSTALL_COMMAND}}": m.get("commands", {}).get("install", "# <TODO: verify>"),
            "{{BUILD_COMMAND}}": m.get("commands", {}).get("build", "# <TODO: verify>"),
            "{{RUN_COMMAND}}": m.get("commands", {}).get("run", "# <TODO: verify>"),
            "{{TEST_COMMAND}}": m.get("commands", {}).get("test", "# <TODO: verify>"),
            "{{TIMESTAMP}}": datetime.now(timezone.utc).isoformat(),
        }
        result = template_content
        for placeholder, value in replacements.items():
            result = result.replace(placeholder, str(value))
        return result

    def _generate_context_json(self) -> None:
        context_file = self.target / "agent-context.json"
        static_section = {
            "_comment": "These fields are set once. Edit manually. Never overwritten by the transformer.",
            "repo_name": self.meta.get("project_name", "Project"),
            "primary_language": self.meta.get("primary_languages", ["Unknown"])[0],
            "language_version": self.meta.get("build_system", "unknown"),
            "frameworks": self.meta.get("frameworks", []),
            "entry_point": self.meta.get("entry_points", [""])[0] if self.meta.get("entry_points") else "",
            "api_protocol": "REST",
            "restricted_write_paths": [],
            "environment_variables": {var: "<description>" for var in self.meta.get("environment_variables", [])},
        }
        dynamic_section = {
            "_comment": "These fields are refreshed automatically on every scan.",
            "last_scanned": datetime.now(timezone.utc).isoformat(),
            "module_layout": {"src/": "Source code", "tests/": "Test code"},
            "domain_concepts": [],
            "agent_capabilities": ["code_analysis", "testing", "building"],
            "test_command": self.meta.get("commands", {}).get("test", "unknown"),
            "build_command": self.meta.get("commands", {}).get("build", "unknown"),
            "lint_command": "",
        }
        if context_file.exists():
            try:
                existing = json.loads(context_file.read_text())
                if "static" in existing:
                    static_section.update(existing.get("static", {}))
            except json.JSONDecodeError:
                pass
        self._write_file("agent-context.json", json.dumps({"static": static_section, "dynamic": dynamic_section}, indent=2, ensure_ascii=False))

    def _generate_agents_md(self) -> None:
        t = TEMPLATES_DIR / "AGENTS.template.md"
        if t.exists():
            self._write_file("AGENTS.md", self._fill_template(t.read_text(encoding="utf-8")))

    def _generate_claude_md(self) -> None:
        t = TEMPLATES_DIR / "CLAUDE.template.md"
        if t.exists():
            self._write_file("CLAUDE.md", self._fill_template(t.read_text(encoding="utf-8")))

    def _generate_system_prompt(self) -> None:
        t = TEMPLATES_DIR / "system_prompt.template.md"
        if t.exists():
            self._write_file("system_prompt.md", self._fill_template(t.read_text(encoding="utf-8")))
        else:
            print("[WARN] system_prompt.template.md not found.")

    def _generate_mcp_json(self) -> None:
        t = TEMPLATES_DIR / "mcp.template.json"
        if t.exists():
            self._write_file("mcp.json", self._fill_template(t.read_text(encoding="utf-8")))

    def _generate_tool_templates(self) -> None:
        lang_map = {
            "Python": ("tool.python.template.py", "tools/example_tool.py"),
            "TypeScript": ("tool.typescript.template.ts", "tools/example_tool.ts"),
            "JavaScript": ("tool.typescript.template.ts", "tools/example_tool.ts"),
            "Java": ("tool.java.template.java", "tools/ExampleTool.java"),
            "Go": ("tool.go.template.go", "tools/example_tool.go"),
        }
        generated = set()
        for lang in self.meta.get("primary_languages", []):
            if lang in lang_map:
                tmpl_name, output = lang_map[lang]
                if output not in generated:
                    t = TEMPLATES_DIR / tmpl_name
                    if t.exists():
                        self._write_file(output, t.read_text(encoding="utf-8"))
                        generated.add(output)

    def _generate_memory_schema(self) -> None:
        schema = {
            "version": "2.3.3",
            "generated_at": datetime.now(timezone.utc).isoformat(),
            "project": self.meta.get("project_name", "Project"),
            "languages": self.meta.get("primary_languages", []),
            "frameworks": self.meta.get("frameworks", []),
            "conventions": {
                "test_framework": self.meta.get("test_framework", "unknown"),
                "entry_points": self.meta.get("entry_points", []),
            },
        }
        self._write_file("memory/schema.md", f"```json\n{json.dumps(schema, indent=2, ensure_ascii=False)}\n```\n")


