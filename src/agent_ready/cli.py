class RepoAnalyzer:
    def __init__(self, target: Path):
        self.target = target.resolve()
        self.name = self.target.name
        self.files: list[Path] = []
        self.languages: dict[str, int] = {}
        self.frameworks: list[str] = []
        self.build_system: str = "unknown"
        self.entry_points: list[str] = []
        self.test_framework: str = "unknown"
        self.test_directory: str = ""
        self.source_dirs: list[str] = []
        self.key_files: dict[str, str] = {}
        self.commands: dict[str, str] = {}
        self.description: str = ""
        self.env_vars: list[str] = []

    def analyze(self) -> dict[str, Any]:
        print(f"🔍 Analyzing repository: {self.target}")
        for root, dirs, files in os.walk(self.target):
            dirs[:] = [d for d in dirs if d not in IGNORE_DIRS]
            for f in files:
                self.files.append(Path(root) / f)
        print(f"  📁 Found {len(self.files)} files")
        self._detect_languages()
        self._detect_build_system()
        self._detect_frameworks()
        self._detect_entry_points()
        self._detect_tests()
        self._detect_source_dirs()
        self._detect_key_files()
        self._detect_env_vars()
        self._read_description()
        self._infer_commands()
        return self._to_dict()

    def _detect_languages(self) -> None:
        for f in self.files:
            ext = f.suffix.lower()
            for lang, exts in LANGUAGE_EXTENSIONS.items():
                if ext in exts:
                    self.languages[lang] = self.languages.get(lang, 0) + 1
                    break
        self.languages = dict(sorted(self.languages.items(), key=lambda x: x[1], reverse=True))
        if self.languages:
            print(f"  🌐 Languages: {', '.join(self.languages.keys())}")

    def _detect_build_system(self) -> None:
        for config_file, info in CONFIG_FILES.items():
            if (self.target / config_file).exists():
                self.build_system = info["build"]
                break
        print(f"  🔧 Build system: {self.build_system}")

    def _detect_frameworks(self) -> None:
        pkg_file = self.target / "package.json"
        if pkg_file.exists():
            try:
                pkg = json.loads(pkg_file.read_text(encoding="utf-8"))
                all_deps = {**pkg.get("dependencies", {}), **pkg.get("devDependencies", {})}
                for framework, indicators in FRAMEWORK_INDICATORS.items():
                    if any(ind in all_deps for ind in indicators):
                        if framework not in self.frameworks:
                            self.frameworks.append(framework)
            except (json.JSONDecodeError, OSError):
                pass
        for req_file in ["requirements.txt", "pyproject.toml", "setup.py"]:
            req_path = self.target / req_file
            if req_path.exists():
                try:
                    content = req_path.read_text(encoding="utf-8").lower()
                    for framework, indicators in FRAMEWORK_INDICATORS.items():
                        if any(ind.lower() in content for ind in indicators):
                            if framework not in self.frameworks:
                                self.frameworks.append(framework)
                except OSError:
                    pass
        for build_file in ["pom.xml", "build.gradle", "build.gradle.kts"]:
            build_path = self.target / build_file
            if build_path.exists():
                try:
                    content = build_path.read_text(encoding="utf-8").lower()
                    for framework, indicators in FRAMEWORK_INDICATORS.items():
                        if any(ind.lower() in content for ind in indicators):
                            if framework not in self.frameworks:
                                self.frameworks.append(framework)
                except OSError:
                    pass
        if self.frameworks:
            print(f"  📦 Frameworks: {', '.join(self.frameworks)}")

    def _detect_entry_points(self) -> None:
        entry_names = ["main", "app", "index", "server", "manage", "cli"]
        for f in self.files:
            if f.stem.lower() in entry_names and f.suffix in (".py", ".ts", ".js", ".go", ".java", ".rb", ".php"):
                rel_path = str(f.relative_to(self.target))
                if rel_path not in self.entry_points:
                    self.entry_points.append(rel_path)
        if self.entry_points:
            print(f"  🚀 Entry points: {', '.join(self.entry_points[:3])}")

    def _detect_tests(self) -> None:
        for d in ["test", "tests", "spec", "__tests__", "test_", "testing"]:
            if (self.target / d).is_dir():
                self.test_directory = d
                break
        if (self.target / "pytest.ini").exists():
            self.test_framework = "pytest"
        elif (self.target / "jest.config.js").exists() or (self.target / "jest.config.ts").exists():
            self.test_framework = "jest"
        elif (self.target / ".rspec").exists():
            self.test_framework = "rspec"

    def _detect_source_dirs(self) -> None:
        for d in ["src", "lib", "app", "pkg", "internal", "cmd", "api"]:
            if (self.target / d).is_dir():
                self.source_dirs.append(d)

    def _detect_key_files(self) -> None:
        key_patterns = {
            "README.md": "Project documentation",
            "package.json": "Node.js dependencies",
            "pyproject.toml": "Python project config",
            "pom.xml": "Maven configuration",
            "build.gradle": "Gradle configuration",
            "Dockerfile": "Container definition",
            ".env.example": "Environment template",
        }
        for filename, desc in key_patterns.items():
            if (self.target / filename).exists():
                self.key_files[filename] = desc

    def _detect_env_vars(self) -> None:
        for env_file in [".env.example", ".env.sample"]:
            env_path = self.target / env_file
            if env_path.exists():
                try:
                    content = env_path.read_text(encoding="utf-8")
                    for line in content.splitlines():
                        if line.strip() and "=" in line and not line.startswith("#"):
                            self.env_vars.append(line.split("=", 1)[0].strip())
                except OSError:
                    pass

    def _read_description(self) -> None:
        readme = self.target / "README.md"
        if readme.exists():
            try:
                content = readme.read_text(encoding="utf-8")
                in_para = False
                para_lines = []
                for line in content.splitlines()[1:]:
                    s = line.strip()
                    if not s and in_para:
                        break
                    if s and not s.startswith("#"):
                        in_para = True
                        para_lines.append(s)
                if para_lines:
                    self.description = " ".join(para_lines)[:300]
            except OSError:
                pass
        if not self.description:
            langs = ", ".join(list(self.languages.keys())[:2]) or "software"
            self.description = f"A {langs} project"

    def _infer_commands(self) -> None:
        cmd_map = {
            "npm": {"install": "npm install", "build": "npm run build", "test": "npm test", "run": "npm start"},
            "pip": {"install": "pip install -r requirements.txt", "build": "python -m build", "test": "pytest", "run": "python -m app"},
            "go": {"install": "go mod download", "build": "go build ./...", "test": "go test ./...", "run": "go run ."},
            "maven": {"install": "mvn install", "build": "mvn package", "test": "mvn test", "run": "mvn exec:java"},
            "gradle": {"install": "./gradlew dependencies", "build": "./gradlew build", "test": "./gradlew test", "run": "./gradlew run"},
        }
        self.commands = cmd_map.get(self.build_system, {})

    def _to_dict(self) -> dict[str, Any]:
        return {
            "project_name": self.name,
            "description": self.description,
            "primary_languages": list(self.languages.keys()),
            "frameworks": self.frameworks,
            "build_system": self.build_system,
            "entry_points": self.entry_points,
            "test_framework": self.test_framework,
            "test_directory": self.test_directory,
            "source_directories": self.source_dirs,
            "key_files": self.key_files,
            "commands": self.commands,
            "environment_variables": self.env_vars,
        }


