# CLI
# ============================================================================

def main() -> None:
    parser = argparse.ArgumentParser(
        description="Transform any repository into an AI-agent-ready codebase.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  agent-ready --target /path/to/repo
  agent-ready --target /path/to/repo --llm
  agent-ready --target /path/to/repo --llm --only agents
  agent-ready --target /path/to/repo --dry-run
  agent-ready --target /path/to/repo --llm --eval
  agent-ready --target /path/to/repo --eval-only
  agent-ready --target /path/to/repo --eval-only --fail-level 0.8
        """,
    )
    parser.add_argument("--target", "-t", required=True, help="Target repository path")
    parser.add_argument(
        "--llm", action="store_true",
        help="LLM-first mode: Claude Opus analyses the code, Claude Sonnet writes every file. Requires ANTHROPIC_API_KEY.",
    )
    parser.add_argument(
        "--only", choices=["agents", "tools", "context", "memory"],
        help="Generate only specific category",
    )
    parser.add_argument("--dry-run", action="store_true", help="Preview without writing")
    parser.add_argument("--force", action="store_true", help="Overwrite existing files")
    parser.add_argument(
        "--provider", choices=["anthropic", "openai", "gemini"], default="anthropic",
        help="LLM provider (static mode only)",
    )
    parser.add_argument("--verbose", "-v", action="store_true", help="Verbose output")
    parser.add_argument("--install-hooks", action="store_true", help="Install pre-commit hook")
    parser.add_argument("--verify", action="store_true", help="Verify generated context with LLM")
    parser.add_argument("--quiet", action="store_true", help="Suppress non-essential output")
    parser.add_argument(
        "--eval", action="store_true",
        help="After transformation, evaluate whether context files improve AI responses. Requires ANTHROPIC_API_KEY.",
    )
    parser.add_argument(
        "--eval-only", action="store_true",
        help="Skip transformation, run evaluation only against existing context files. Requires ANTHROPIC_API_KEY.",
    )
    parser.add_argument(
        "--fail-level", type=float, default=0.0, metavar="0.0-1.0",
        help="Exit 1 if eval pass rate is below this threshold. E.g. 0.8 = 80%%. Use as CI gate.",
    )

    args = parser.parse_args()
    target = Path(args.target).resolve()

    if not target.is_dir():
        print(f"❌ Directory not found: {target}")
        sys.exit(1)

    if args.install_hooks:
        install_hooks(target)
        return

    if args.verify:
        verify(target)
        return

    # ── Eval-only path — skip transformation entirely ─────────────────────────
    if args.eval_only:
        if not args.quiet:
            print()
            print("╔══════════════════════════════════════════════╗")
            print("║   🧪 AgentReady Evaluator v2.0              ║")
            print("╚══════════════════════════════════════════════╝")
            print()
        _run_eval_pipeline(target=target, fail_level=args.fail_level, quiet=args.quiet)
        return

    if not args.quiet:
        print()
        print("╔══════════════════════════════════════════════╗")
        if args.llm:
            print("║   🤖 AgentReady Transformer v2.0 (LLM)     ║")
        else:
            print("║   🤖 AgentReady Transformer v2.0           ║")
        print("╚══════════════════════════════════════════════╝")
        print()

    # ── LLM-first path ────────────────────────────────────────────────────────
    if args.llm:
        generated = _run_llm_pipeline(
            target=target, only=args.only, dry_run=args.dry_run,
            force=args.force, quiet=args.quiet,
        )

    # ── Static / template path ────────────────────────────────────────────────
    else:
        analyzer = RepoAnalyzer(target)
        metadata = analyzer.analyze()
        if not args.quiet:
            print()
        generator_obj = AgenticGenerator(target, metadata, dry_run=args.dry_run, force=args.force)
        generated = generator_obj.generate_only(args.only) if args.only else generator_obj.generate_all()

    # ── Scoring + summary ─────────────────────────────────────────────────────
    readiness = score(target)

    if not args.quiet:
        print()
        print("─" * 50)
        mode_str = "[DRY RUN] " if args.dry_run else ""
        print(f"✅ {mode_str}Transformation Complete")
        print("─" * 50)
        print()
        print("  Generated Files:")
        for filepath, status in generated:
            print(f"    {status[:2]} {filepath}")
        print()
        print("──────────────────────────────────────────────")
        print(f"  AGENTIC READINESS SCORE: {readiness['score']} / {readiness['max']}")
        print("──────────────────────────────────────────────")
        for criterion, points in readiness["rows"]:
            print(f"  {criterion:<42} {points:>4}")
        if readiness["score"] < 100:
            print()
            print("  💡 To improve your score:")
            if not (target / "agent-context.json").exists():
                print("     - Generate agent-context.json (run without --only flag)")
            if not any(target.glob("**/openapi.{yaml,json,yml}")):
                print("     - Add an OpenAPI spec (openapi.yaml)")
            if not args.llm:
                print("     - Re-run with --llm for real content instead of placeholders")
        print("──────────────────────────────────────────────")
        print()

    # ── Optional eval after transformation ────────────────────────────────────
    if args.eval and not args.dry_run:
        _run_eval_pipeline(target=target, fail_level=args.fail_level, quiet=args.quiet)


if __name__ == "__main__":
    main()